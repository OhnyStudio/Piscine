int	ft_find_next_prime(int nb)
{
	int	i;

	i = 3;
	if (nb < 3)
		return (2);
	if (nb >= 2147483630)
		return (2147483647);
	if (nb % 2 == 0)
		nb++;
	while (i * i <= nb)
	{
		if (nb % i == 0)
		{
			nb += 2;
			i = 3;
		}
		else
		{
			i++;
		}
	}
	return (nb);
}
