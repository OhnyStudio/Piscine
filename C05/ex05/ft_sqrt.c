int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 1)
		return (1);
	i = 1;
	if (nb > 0)
	{
		while (i <= nb / 2)
		{
			if (i * i == nb)
			{
				return (i);
			}
			else if (i * i > nb)
			{
				return (0);
			}
			i++;
		}
	}
	return (0);
}
